sealed class Screen(val route: String) {
    object Dashboard : Screen("dashboard")
    object AppUsage : Screen("app_usage")
    object ScrollStats : Screen("scroll_stats")
    object Settings : Screen("settings")
}

data class BottomNavItem(
    val route: String,
    val icon: ImageVector,
    val label: String
)

val bottomNavItems = listOf(
    BottomNavItem(Screen.Dashboard.route, Icons.Default.Dashboard, "Dashboard"),
    BottomNavItem(Screen.AppUsage.route, Icons.Default.Apps, "App Usage"),
    BottomNavItem(Screen.ScrollStats.route, Icons.Default.Timeline, "Scroll Stats"),
